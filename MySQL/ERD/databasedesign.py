# What is the point?
# When we normalize our tables, we do not repeat data. This means that in the long term, we can use our storage space more efficiently. 

# There is also another advantage that we obtain by normalizing our tables and establishing relationships between them. Later we will learn that  IDENTIFIERS  and  FOREIGN KEYS  serve as the glue between our tables. With SQL, we can manipulate our tables and create the custom table we need for the job in question. 

# By dividing our data into different tables, we make each table good at one thing: store instances or rows of that data. In addition, if we separate our tables, our database becomes more modular. This means that we can create our own custom tables depending on the task in question using SQL. 